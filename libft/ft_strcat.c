#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	char	*p;
	int		i;
	int		a;

	p = s1;
	i = 0;
	a = 0;
	while (s1[i] > '\0')
		i++;
	while (s2[a] != '\0')
	{
		s1[i] = s2[a];
		i++;
		a++;
	}
	s1[i] = '\0';
	return (p);
}
